/*Manera sintaxis flecha de escribir componentes funcionales*/

export const suma = (a,b)=> a + b

//Sintaxis identificador
export const resta = function(a,b){
    return a-b
}

//Sintaxis legada
export function multiplicar(a,b){
    return a*b
}

export function division(a,b){
    return a/b
}

export function elevacion(a,b){
    return a**(b)
}